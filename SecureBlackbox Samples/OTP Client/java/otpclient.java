import java.io.*;
import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

import java.nio.charset.StandardCharsets;
import secureblackbox.*;

public class otpclient extends ConsoleDemo {
	private static String optval(String[] args, String option) {
		for (int x = 0; x < args.length - 1; x++) {
			if (args[x].equalsIgnoreCase(option)) {
				return args[x + 1];
			}
		}
		return "";
	}

	private static boolean optext(String[] args, String option) {
		for (int x = 0; x < args.length; x++) {
			if (args[x].equalsIgnoreCase(option)) {
				return true;
			}
		}
		return false;
	}

	private static void displayHelp(String errMes) {
		System.out.println(
				"NAME\n" +
				"  otpclient -- SecureBlackbox OTPClient Demo Application\n\n" +
				"SYNOPSIS\n" +
				"  otpclient <-alg algorithm> [-secret key_secret] [-len pass_length] [-interval interval]\n\n" +
				"DESCRIPTION\n" +
				"  This sample acts as a basic One-Time-Password protocol client.\n" +
				"  The options are as follows:\n\n" +
				"  -alg       The otp algorithm (Required). Valid values:\n\n" +
				"                0 - HOTP\n" +
				"                1 - TOTP\n\n" +
				"  -secret     The key secret (Required).\n\n" +
				"  -len        The password length.\n\n" +
				"  -interval   The interval value.\n\n" +
				"EXAMPLES\n" +
				"  otpclient -alg 0 -secret testsecret \n\n" +
				"  otpclient -alg 1 -secret testsecret -len 8 -interval 56 \n"
		);

		if (errMes.length() > 0) {
			System.out.println("Error: " + errMes);
			System.out.println();
		}

		confirmExit();
	}

	private static void confirmExit() {
		System.out.println("Press Enter to exit the demo.");
		input();
	}

	public static void main(String[] args) {
		if (args.length == 0) {
			displayHelp("");
			return;
		}

		OTPClient client = new OTPClient();
		Utils utils = new Utils();
		int algorithm;
		int interval = 0;

		try {
			if (optext(args, "-alg")) {
				algorithm = Integer.parseInt(optval(args, "-alg"));
			} else {
				displayHelp("-alg is required.");
				return;
			}

			if (optext(args, "-secret")) {
				client.setKeySecret(optval(args, "-secret").getBytes(StandardCharsets.UTF_8));
			} else {
				displayHelp("-secret is required.");
				return;
			}

			if (optext(args, "-len")) {
				client.setPasswordLength(Integer.parseInt(optval(args, "-len")));
			} else {
				client.setPasswordLength(10);
			}

			if (optext(args, "-interval")) {
				interval = Integer.parseInt(optval(args, "-interval"));
			}

			// Calculate password
			switch (algorithm) {
				case 0:
					System.out.println("Calculated password: " + client.generateHOTPPassword(interval));
					break;
				case 1:
					System.out.println("Calculated password: " + client.generateTOTPPassword(interval, "SHA256"));
					break;
				default:
					displayHelp("Invalid alg value.");
					return;
			}
			System.out.println();

			confirmExit();
		} catch (Exception ex) {
			ConsoleDemo.displayError(ex);
		}
	}
}

